export const getVehicleById = (id) => {
  return vehicles.find(vehicle => vehicle.id.toString() === id.toString());
};

export const searchVehicles = (query) => {
  const searchTerm = query.toLowerCase().trim();
  return vehicles.filter(vehicle => 
    vehicle.name.toLowerCase().includes(searchTerm) ||
    vehicle.brand.toLowerCase().includes(searchTerm) ||
    vehicle.type.toLowerCase().includes(searchTerm)
  );
};

export const getVehiclesByType = (type) => {
  return vehicles.filter(vehicle => 
    vehicle.type.toLowerCase() === type.toLowerCase()
  );
};

